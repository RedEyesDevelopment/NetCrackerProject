  FUNCTION is_date_leap_year
    (d DATE)
    RETURN NUMBER
  IS
    y NUMBER(4);
    BEGIN
      y := EXTRACT (YEAR FROM d);
      IF (MOD(y,400) = 0) THEN RETURN 1; END IF;
      IF (MOD(y,100) = 0) THEN RETURN 0; END IF;
      IF (MOD(y,4) = 0) THEN RETURN 1; END IF;
      RETURN 0;
